#include "SpaceBattle.h"
#include "Explosion.h"
#include "Random.h"
#include "Hud.h"

// ---------------------------------------------------------------------------------

Explosion::Explosion(float pX, float pY)
{
    // número aleatório entre 0 e 1
    RandF color{ 0, 1 };

    // configura emissor de partículas
    Generator explosion;
    explosion.imgFile       = "Resources/Player/particle_fire.png";    // arquivo de imagem
    explosion.angle         = 0.0f;                     // direção da explosão
    explosion.spread        = 360.0;                   // espalhamento em graus
    explosion.lifetime      = 0.50f;                    // tempo de vida em segundos
    explosion.frequency     = 0.000f;                   // tempo entre geração de novas partículas
    explosion.percentToDim  = 0.5f;                     // desaparece após 60% da vida
    explosion.minSpeed      = 25.0f;                    // velocidade mínima das partículas
    explosion.maxSpeed      = 250.0f;                   // velocidade máxima das partículas
    explosion.color.r       = 1.0f;                     // cor da partícula entre 0 e 1
    explosion.color.g       = 1.0f;                     // cor da partícula entre 0 e 1
    explosion.color.b       = 1.0f;                     // cor da partícula entre 0 e 1
    explosion.color.a       = 1.0f;                     // transparência da partícula

    // configura emissor de partículas
    Generator explosion2;
    explosion.imgFile       = "Resources/Player/particle_fire2.png";    // arquivo de imagem
    explosion.angle         = 0.0f;                     // direção da explosão
    explosion.spread        = 360.0;                    // espalhamento em graus
    explosion.lifetime      = 0.50f;                    // tempo de vida em segundos
    explosion.frequency     = 0.000f;                   // tempo entre geração de novas partículas
    explosion.percentToDim  = 0.5f;                     // desaparece após 60% da vida
    explosion.minSpeed      = 25.0f;                    // velocidade mínima das partículas
    explosion.maxSpeed      = 250.0f;                   // velocidade máxima das partículas
    explosion.color.r       = 1.0f;                     // cor da partícula entre 0 e 1
    explosion.color.g       = 1.0f;                     // cor da partícula entre 0 e 1
    explosion.color.b       = 1.0f;                     // cor da partícula entre 0 e 1
    explosion.color.a       = 1.0f;                     // transparência da partícula

    // cria sistema de partículas
    sparks1 = new Particles(explosion);
    
    // gera 50 partículas na posição indicada
    sparks1->Generate(pX, pY, 100);
    type = EXPLOSION;
    
    // ---------------------------------------------------

    // cria sistema de partículas
    sparks2 = new Particles(explosion);
    // gera 50 partículas na posição indicada
    sparks2->Generate(pX, pY, 100);
    type = EXPLOSION;


    // incrementa contagem de partículas
    Hud::particles += 50;
}

// ---------------------------------------------------------------------------------

Explosion::~Explosion()
{
    delete sparks1;
    delete sparks2;
    // decrementa contagem de partículas
    Hud::particles -= 50;
}

// -------------------------------------------------------------------------------

void Explosion::Update()
{
    // atualiza posição de cada partícula
    sparks1->Update(gameTime);
    sparks2->Update(gameTime);

    // remove da cena quando todas as partículas estão inativas
    if (sparks1->Inactive())
        SpaceBattle::scene->Delete();
    if (sparks2->Inactive())
        SpaceBattle::scene->Delete();
}

